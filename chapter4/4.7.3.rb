# 配列の演算
a = [1]
b = [2,3]
a.concat(b) # => [1,2,3]
# aは破壊的に変更される
puts a # => [1,2,3]

a = [1]
b = [2,3]
a + b # => [1,2,3]
# aは変更されない
puts('__')
puts a # => [1]

# 集合
# 和集合|,差集合-,積集合&
a = [1,2,3]
b = [3,4,5]
puts('__')
puts a|b # => [1,2,3,4,5]
puts('__')
puts a-b # => [1,2]
puts('__')
puts a&b # => [3]
